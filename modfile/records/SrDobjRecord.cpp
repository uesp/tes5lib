/*===========================================================================
 *
 * Class CSrDobjRecord Event - void OnAddSubrecord (pSubrecord);
 *
 *=========================================================================*/
void CSrDobjRecord::OnAddSubrecord (CSrSubrecord* pSubrecord) {

	if (pSubrecord->GetRecordType() == SR_NAME_DNAM)
	{
		m_pDnamData = SrCastClass(CSrDataSubrecord, pSubrecord);
	}

	else
	{
	CSrRecord::OnAddSubrecord(pSubrecord);
	}

}
/*===========================================================================
 *		End of Class Event CSrDobjRecord::OnAddSubRecord()
 *=========================================================================*/


/*===========================================================================
 *
 * Class CSrDobjRecord Event - void OnDeleteSubrecord (pSubrecord);
 *
 *=========================================================================*/
void CSrDobjRecord::OnDeleteSubrecord (CSrSubrecord* pSubrecord) {

	if (m_pDnamData == pSubrecord)
		m_pDnamData = NULL;

	else
		CSrRecord::OnDeleteSubrecord(pSubrecord);

}
/*===========================================================================
 *		End of Class Event CSrDobjRecord::OnDeleteSubrecord()
 *=========================================================================*/


